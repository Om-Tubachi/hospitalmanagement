export default function DashboardPage() {
  const { user } = useAuth();

  if (!user) return null;

  const getDashboardTitle = () => {
    switch (user.role) {
      case 'super_admin': return 'System Overview';
      case 'doctor': return 'Doctor Dashboard';
      case 'nurse': return 'Nurse Dashboard';
      case 'patient': return 'Patient Portal';
      default: return 'Dashboard';
    }
  };

  const getDashboardDescription = () => {
    switch (user.role) {
      case 'super_admin': return 'Monitor system performance and manage users';
      case 'doctor': return 'Manage your patients and appointments';
      case 'nurse': return 'Track your tasks and patient care activities';
      case 'patient': return 'Access your medical records and book appointments';
      default: return 'Welcome to your dashboard';
    }
  };

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-3xl font-bold text-gray-900">{getDashboardTitle()}</h1>
        <p className="text-gray-600">{getDashboardDescription()}</p>
      </div>

      {user.role === 'super_admin' && <SuperAdminDashboard />}
      {user.role === 'doctor' && <DoctorDashboard />}
      {user.role === 'nurse' && <NurseDashboard />}
      {user.role === 'patient' && <PatientDashboard />}
    </div>
  );
}